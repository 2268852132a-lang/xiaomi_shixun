#include "e3.h"
#include "i2c.h"

/*********************************************************************************************************
函数名:     e3_init
入口参数:   i2c初始地址 address 
出口参数:   无 
返回值:     i2c_addr_def定义结构体
作者:       ZZZ
日期:       2023/4/1
调用描述:   得到e3 i2c地址,若器件不存在则结构体flag值为0,若存在则初始化芯片该结构体flag值为1
**********************************************************************************************************/
i2c_addr_def e3_init(uint8_t address)
{
    i2c_addr_def e3_address;
    
    e3_address = get_board_address(address);
    
    return e3_address;
}

/*********************************************************************************************************
函数名:     e3_all_init
入口参数:   e3_address结构体指针  e3_addr:e3起始地址 
出口参数:   e3_address结构体指针
返回值:     无
作者:       ZZZ
日期:       2023/4/1
调用描述:   查找并初始化所有e3子板,并把相应的地址放在结构体指针e3_address
**********************************************************************************************************/
void e3_all_init(e3_addr_def *e3_address, uint8_t e3_addr)
{
    uint8_t i;
    
    for(i = 0; i < 4; i++) {
        e3_address->curtain_addr[i] = get_board_address(e3_addr + i * 2);
    }
}

/*********************************************************************************************************
函数名:     e3_set_position
入口参数:   i2c_periph I2C口选择 i2c0 or i2c1  i2c_addr 器件地址  re_pos 目标位置 0-100
出口参数:   无 
作者:       ZZZ
日期:       2023/3/28
描述:      控制窗帘位置 0=完全关闭 100=完全打开
***********************************************************************************************************/
void e3_set_position(uint32_t i2c_periph, uint8_t i2c_addr, uint8_t re_pos)
{
    if(re_pos > 100) re_pos = 100;
    
    // 向 0x03 寄存器写入位置值
    i2c_byte_write(i2c_periph, i2c_addr, 0x03, re_pos);
}

/*********************************************************************************************************
函数名:     e3_get_position
入口参数:   i2c_periph I2C口选择 i2c0 or i2c1  i2c_addr 器件地址
出口参数:   无 
返回值:     当前位置 0-100
作者:       ZZZ
日期:       2023/3/28
描述:      读取窗帘当前位置
***********************************************************************************************************/
uint8_t e3_get_position(uint32_t i2c_periph, uint8_t i2c_addr)
{
    uint8_t position;
    
    // 从 0x01 寄存器读取当前位置
    i2c_read(i2c_periph, i2c_addr, 0x01, &position, 1);
    
    return position;
}
/*********************************************************************************************************
函数名:     e3_stop
入口参数:   i2c_periph I2C口, i2c_addr 器件地址
描述:      急停，电机立刻停止
**********************************************************************************************************/
void e3_stop(uint32_t i2c_periph, uint8_t i2c_addr)
{
    // 向位置寄存器写入 0xFF 触发急停
    // 根据实际 E3 规格书，停止命令可能是写 0xFF 或其他值
    i2c_byte_write(i2c_periph, i2c_addr, 0x03, 0xFF);
}